import java.util.*;
public class relationalOp{
    public static void main(String args[]){
        int A = 30;
        int B = 34;

        System.out.println(A == B);
        System.out.println(A != B);
        System.out.println(A>B);
        System.out.println(A<B);
        System.out.println(A>=B);
        System.out.println(A<=B);
    }
}
